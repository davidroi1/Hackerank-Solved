# Ce programme retourne la somme, la difference et le produit de deux nombre

# recuperation de donnee de l'utilisation entree en input
nb1 = int(input("Enter a first number: "))
nb2 = int(input("Enter a second number: "))

# affichage des differente operation
print(nb1 + nb2)
print(nb1 - nb2)
print(nb1 * nb2)